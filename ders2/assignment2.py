"""
Öz seçiminizlə iki tam ədəd üçün A və B sabitlərini müəyyən edin.
A və B arasında aşağıdakı əməliyyatları yerinə yetirin və nəticələri çap edin:
Toplama (+)
Çıxma (-)
Vurma (*)
Bölmə - normal (/)
Bölmə - tam hissə (//)
Qalıq (%)
Dərəcəyə yüksəltmə (**)
Hər əməliyyatın qısa izahını (şərh olaraq) əlavə edin.
"""

A = 23
B = 7

print(A + B) #toplama (=30)
print(A - B) #cixma (=16)
print(A * B) #vurma (=161)
print(A / B) #bolme (=3.285....)
print(A // B) #tam qismet (=3)
print(A % B) #qaliq (=2)
print(A ** B) #dereceye yukseltme (=3404825447)
